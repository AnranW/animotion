<h2>What is Keynote?</h2>

<p>
	Using <b>Keynote</b> you can make slides for presentations and educational content you can record
	and share with everyone using web technologies like
	<b>HTML, CSS, and JavaScript</b> including any JavaScript library to visualize what you want from using
	the Canvas API to Three.js.
</p>

<p>
	<b>Keynote</b> uses <a href="https://svelte.dev/">Svelte</a>,
	<a href="https://revealjs.com/">Reveal.js</a>
	and <a href="https://tailwindcss.com/">Tailwind CSS</a> to create beautiful animated presentations
	with ease thanks to <a href="/docs/auto-animate">auto-animate</a> from Reveal.js â€” you don't have
	to know Svelte but it's easy to learn and powerful if you want to learn a JavaScript framework.
	You can learn Svelte from the <a href="https://learn.svelte.dev/">official Svelte tutorial.</a>
</p>

<p>
	If you don't love Tailwind CSS you can write regular CSS but give it a chance because of the speed
	it gives you when making presentations.
</p>
